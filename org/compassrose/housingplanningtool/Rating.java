package org.compassrose.housingplanningtool;

public class Rating {
	int stars;
    String content;
    
    public Rating()
    {

    }

    public int getStars()
    {
        return stars;
    }

    public void setStars(int s)
    {
        stars = s;
    }

    public String getContent()
    {
        return content;
    }

    public void setContent(String c)
    {
        content = c;
    }

    
}
